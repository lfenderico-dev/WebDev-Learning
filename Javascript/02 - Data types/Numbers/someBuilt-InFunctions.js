"use strict";

// 1. Math.random()
    // Return a nrandom number from 0 to 1 (not including one).
alert( Math.random() ); // any random number


// 2. Math.max()
    // Returns the greatest number in the argument:
alert( Math.max(3, 5, -10, 0) ); // 5


// 3. Math.min()
    // Returns the smallest number in the argument:
alert( Math.min(1, 2) ); // 1


// 4. Math.pow(n, power);
    // Returns a n (number) raised to the given power:
alert( Math.pow(2, 2) ); // 4