"use strict";

// Syntax:
/* 
    for (begin; condition; step) {
        code
    }

1. Begin only execute when entering the loop.
2. Condition is checked after every iteration.
3. Code runs while condition is true.
4. Step executes after the body on every iteration.
*/

// Example:
let x;

for (x = 0; x < 18 ; x *= 2) {
    alert(x);
    
}
// Starts at x=0;
// Last until x >= 18;
// Every iteration x is doubled


